<?php

//OPERATOR MATEMATIKA
$a = 8;
$b = 3;

echo "a = $a, b = $b\n";
echo "a + b = " . ($a + $b) . "\n";   // Penjumlahan = 13
echo "a - b = " . ($a - $b) . "\n";   // Pengurangan = 7
echo "a * b = " . ($a * $b) . "\n";   // Perkalian   = 30
echo "a / b = " . ($a / $b) . "\n";   // Pembagian   = 3.333...
echo "a % b = " . ($a % $b) . "\n";   // Sisa bagi   = 1
echo "a ** b = " . ($a ** $b) . "\n"; // Pangkat     = 1000